#Suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5

#multiplicacion y division
multiplicacion = 12 * 5
division = 12 / 5 #siempre devuelve float aunque el resultado sea un entero

#Potencia  (exponente **)
exponente = 12 ** 5

#division baja (//)
division_baja= 12//5 #devuelve entero redondeado hacia abajo

#Modulo o resto
modulo = 12 % 5

tipo_dato = type (division_baja)

print(suma)
print(resta)
print(multiplicacion)
print(division)
print(exponente)
print(division_baja)
print(modulo)
print(tipo_dato)